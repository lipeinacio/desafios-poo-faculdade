public class Veiculo {
    
    private int placa;
    private String procedencia;
    private String marca;
    private String modelo;
    private double nModelo;
    private int anoFabricação;
    private String chassi;
    private String renavam;
    private String combustivel;
    private String motor;
    private String quilometragem;
    private String consumoMedio;

    
    public int getPlaca() {
        return placa;
    }
    public void setPlaca(int placa) {
        this.placa = placa;
    }
    public String getProcedencia() {
        return procedencia;
    }
    public void setProcedencia(String procedencia) {
        this.procedencia = procedencia;
    }
    public String getMarca() {
        return marca;
    }
    public void setMarca(String marca) {
        this.marca = marca;
    }
    public String getModelo() {
        return modelo;
    }
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }
    public double getnModelo() {
        return nModelo;
    }
    public void setnModelo(double nModelo) {
        this.nModelo = nModelo;
    }
    public int getAnoFabricação() {
        return anoFabricação;
    }
    public void setAnoFabricação(int anoFabricação) {
        this.anoFabricação = anoFabricação;
    }
    public String getChassi() {
        return chassi;
    }
    public void setChassi(String chassi) {
        this.chassi = chassi;
    }
    public String getRenavam() {
        return renavam;
    }
    public void setRenavam(String renavam) {
        this.renavam = renavam;
    }
    public String getCombustivel() {
        return combustivel;
    }
    public void setCombustivel(String combustivel) {
        this.combustivel = combustivel;
    }
    public String getMotor() {
        return motor;
    }
    public void setMotor(String motor) {
        this.motor = motor;
    }
    public String getQuilometragem() {
        return quilometragem;
    }
    public void setQuilometragem(String quilometragem) {
        this.quilometragem = quilometragem;
    }
    public String getConsumoMedio() {
        return consumoMedio;
    }
    public void setConsumoMedio(String consumoMedio) {
        this.consumoMedio = consumoMedio;
    }

}
