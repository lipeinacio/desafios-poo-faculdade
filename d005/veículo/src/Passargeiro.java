public class Passargeiro extends Veiculo{
    private String corEx;
    private String corIn;
    private String nPortas;
    private String nPassageiros;
    private String opcionais;

    
    public String getCorEx() {
        return corEx;
    }
    public void setCorEx(String corEx) {
        this.corEx = corEx;
    }
    public String getCorIn() {
        return corIn;
    }
    public void setCorIn(String corIn) {
        this.corIn = corIn;
    }
    public String getnPortas() {
        return nPortas;
    }
    public void setnPortas(String nPortas) {
        this.nPortas = nPortas;
    }
    public String getnPassageiros() {
        return nPassageiros;
    }
    public void setnPassageiros(String nPassageiros) {
        this.nPassageiros = nPassageiros;
    }
    public String getOpcionais() {
        return opcionais;
    }
    public void setOpcionais(String opcionais) {
        this.opcionais = opcionais;
    }
}
